class ArithmeticOperators{
	public static void main(String args[]){
		int x = 10;
		int y = 5;
		
		System.out.println("X= " +x);
		System.out.println("Y= " +x);
		
		System.out.println("X+y = "+ (x+y));
		System.out.println("X-y = "+ (x-y));
		System.out.println("X*y = "+ (x*y));
		System.out.println("X/y = "+ (x/y));
		System.out.println("X%y = "+ (x%y));
	}
}